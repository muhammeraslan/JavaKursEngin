package Day01;

public class WhileLoop {
    public static void main(String[] args) {

        //while
        int i = 2;
        while (i<10) {
            System.out.println(i);
            i += 2;
        }
        // Do-While

        int j =1;
        do{
            System.out.println(j);
            j++;

        }while (j<10);

        }
    }

